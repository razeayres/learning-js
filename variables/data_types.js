/**
 * this is to show how to use
 * different datatypes in
 * Javascript
 */

// string
var stringDemo = "This is a string"
console.log("String:", stringDemo)

// integer
var integerDemo = 19
console.log("Integer:", integerDemo)

// float
var floatDemo = 6.5
console.log("Float:", floatDemo)

// boolean
var booleanDemo = false // it could be true
console.log("Boolean:", booleanDemo)

// null (nothing)
var nullDemo = null
console.log("Null:", nullDemo)

// undefined
var undefinedDemo
console.log("Undefined:", undefinedDemo)

var undefinedAssignedDemo = undefined
console.log("Undefined assigned:", undefinedAssignedDemo)

// object (basic object, not class)
const objectDemo = {
    color: "green",
    name: "Rodrigo",
    age: 40,
}
console.log("Object:", objectDemo)

// array
const arrayDemo = ["um", "dois", "tres", "quatro", "cinco"]
console.log("Array:", arrayDemo)